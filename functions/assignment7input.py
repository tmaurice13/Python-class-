# multiplication
def of(a,b):
    return a*b
def multipy(n1,n2):
    print("The Product is:  ", of(int(n1) , int(n2)))
n1=input("Enter first number: ")
n2=input("Enter second number:  ")
multipy(n1,n2)

# subtraction
def sub(a,b):
    return a-b
